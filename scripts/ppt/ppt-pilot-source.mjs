export function isPilotRequest(fileKey, nodeId, slides) {
  if (fileKey !== PILOT_FILE_KEY || nodeId !== PILOT_NODE_ID) return false;
  const requested = [...slides].sort();
  const expected = [...PILOT_SLIDE_IDS].sort();
  return requested.length === expected.length && requested.every((slide, index) => slide === expected[index]);
}
